# Armazena o valor 5 em uma variável e o valor 2 em outra variável
a = 5
b = 2

# Imprime o tipo de dados dessas duas variáveis
print(f"O tipo de dado da variável 'a' é: {type(a)}")
print(f"O tipo de dado da variável 'b' é: {type(b)}")

# Divide a primeira variável (com o valor 5) pela segunda variável (com o valor 2) e armazena o resultado em uma terceira variável
c = a / b

# Imprime a terceira variável e também o seu tipo
print(f"O valor da variável 'c' é: {c}")
print(f"O tipo de dado da variável 'c' é: {type(c)}")