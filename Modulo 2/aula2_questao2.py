# Armazena o texto "o resultado é" em uma variável, o valor 10 em outra variável, e o valor 3.5 numa terceira variável
texto = "o resultado é"
valor1 = 10
valor2 = 3.5

# Soma os valores da segunda e terceira variável e armazena em outra variável
resultado = valor1 + valor2

# Imprime todas as variáveis na ordem de criação e também seus tipos
print(f"Variável 'texto': {texto}, Tipo: {type(texto)}")
print(f"Variável 'valor1': {valor1}, Tipo: {type(valor1)}")
print(f"Variável 'valor2': {valor2}, Tipo: {type(valor2)}")
print(f"Variável 'resultado': {resultado}, Tipo: {type(resultado)}")